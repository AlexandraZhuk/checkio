def count_neighbours(grid, row, col):
    left = max(0, col-1)
    right = min(len(grid), col+2)
    top = max(0, row-1)
    bottom = min(len(grid), row+2)

    neighbourhood = (cell for line in grid[top:bottom] for cell in line[left:right])

    return sum(neighbourhood) - grid[row][col]


